
public class Colour {

	private double r;
	private double g;
	private double b;
	
	public Colour() {
		this.r = 255;
		this.g = 0;
		this.b = 0;
	}
	
	public Colour(double r, double g, double b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public double getR() {
		return r;
	}

	public double getG() {
		return g;
	}

	public double getB() {
		return b;
	}
	
	
}
