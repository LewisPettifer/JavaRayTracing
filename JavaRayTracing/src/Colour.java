
public class Colour {

	private int r;
	private int g;
	private int b;
	
	public Colour() {
		this.r = 255;
		this.g = 0;
		this.b = 0;
	}
	
	public Colour(int r, int g, int b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public int getR() {
		return r;
	}

	public int getG() {
		return g;
	}

	public int getB() {
		return b;
	}
	
	
}
